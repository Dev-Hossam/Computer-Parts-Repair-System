    private void loginbuttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginbuttonMouseClicked
        User log = new User();
        UserController user = new UserController(log);
        try {
            String username;
            String password;
            username = UserNameField.getText();
            password = String.valueOf(PasswordField.getPassword());

            if (username.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter username and password.");
                return;
            }

            if (user.authenticateUser(username, password) == -1) {
                if (counter == 3) {
                    JOptionPane.showMessageDialog(null, "too many tries", "Something went wrong", JOptionPane.ERROR_MESSAGE);
                    System.exit(0);
                }
                JOptionPane.showMessageDialog(null, "Invalid login try number: " + counter, "Something went wrong", JOptionPane.INFORMATION_MESSAGE);
                counter++;
            } else if (UserController.authenticateUser(username, password) == 3) {
                try {
                    new CustomerMenu().setVisible(true);
                    this.setVisible(false);
                } catch (Exception e) {
                    // Handle the exception appropriately, for example, logging it
                    JOptionPane.showMessageDialog(null, "Something went wrong", "Something went wrong", JOptionPane.ERROR_MESSAGE);
                }
            } else if (UserController.authenticateUser(username, password) == 2) {
                try {
                    new gui_technician().setVisible(true);
                    this.setVisible(false);
                } catch (Exception e) {
                    // Handle the exception appropriately, for example, logging it
                    JOptionPane.showMessageDialog(null, "Something went wrong", "Something went wrong", JOptionPane.ERROR_MESSAGE);
                }
            } else if (UserController.authenticateUser(username, password) == 1) {
                try {
                    new gui_admin().setVisible(true);
                    this.setVisible(false);
                } catch (Exception e) {

                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Something went wrong", "Something went wrong", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_loginbuttonMouseClicked

