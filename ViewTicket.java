    public void populateTable() throws IOException {
        String columns[] = {"Ticket ID",
            "Computer Part",
            "Ticket Status",
            "Ticket Priority",
            "Feedback",
            "Date",
            "Price"
        };

        String[][] data = new String[Customer.getAllCustomers().get(User.currentUser).getCustTickets().size()][];
        for (int i = 0; i < Customer.getAllCustomers().get(User.currentUser).getCustTickets().size(); i++) {
            Ticket ViewTicket = Customer.getAllCustomers().get(User.currentUser).getCustTickets().get(i);
            data[i] = new String[]{
             String.valueOf  (ViewTicket.getTicketId()),
                (ViewTicket.getDevicePart().getName()),
                String.valueOf(ViewTicket.getStatus()),
                ViewTicket.getPriority().toString(),
                (ViewTicket.getFeedback() != null) ? ViewTicket.getFeedback().getContent() : "",
                ViewTicket.getDate().toString(),
                String.valueOf(ViewTicket.getPrice())
            };
        }
        DefaultTableModel model = new DefaultTableModel(data, columns);
        jTable1.setModel(model);
    }
