    public void populateTable() throws IOException {
        String columns[] = {"Ticket ID",
            "Customer name",
            "Customer ID",
            "Technician name",
            "Technician ID",
            "Part Name",
            "Ticket status",
            "Ticket priority",
            "Date",
            "Price",
        };
        
        
        String[][] data = new String[Technician.getAllTechnicians().get(Technician.currentTechnicion).getTechTickets().size()][];
        for (int i = 0; i < Technician.getAllTechnicians().get(Technician.currentTechnicion).getTechTickets().size(); i++) {
            Ticket ticket = Technician.getAllTechnicians().get(Technician.currentTechnicion).getTechTickets().get(i);
            data[i] = new String[]{
               String.valueOf(ticket.getTicketId()),
                ticket.getCustomer().getUsername(),
                String.valueOf(ticket.getCustomer().getUserID()),
                ticket.getTechnician().getUsername(),
                String.valueOf(ticket.getTechnician().getUserID()),
                ticket.getDevicePart().getName(),
                String.valueOf(ticket.getStatus()),
                String.valueOf(ticket.getPriority()),
                String.valueOf(ticket.getDate()),
                String.valueOf(ticket.getPrice())
            };
        }
        DefaultTableModel model = new DefaultTableModel(data, columns);
        jTable1.setModel(model);

    }


