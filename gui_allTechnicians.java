    private void jButtonBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBackActionPerformed

       
        for (int count = 0; count < jTable1.getRowCount(); count++) {
            int Technicianid = Integer.parseInt(jTable1.getValueAt(count, 0).toString());
            boolean availablity = Boolean.parseBoolean((String) jTable1.getValueAt(count, 1));
            String email = (String) jTable1.getValueAt(count, 2);
            String username = (String) jTable1.getValueAt(count, 3);
            String password = (String) jTable1.getValueAt(count, 4);
            String Expertise = (String) jTable1.getValueAt(count, 5);

            AdminController.modifyTechnicianAccount(Technicianid, availablity, email, username, password, Expertise);
        }
    }//GEN-LAST:event_jButtonBackActionPerformed

    private void jButtonRemoveCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveCustomerActionPerformed
        try {
            int technicianID = Integer.parseInt((String) jTable1.getValueAt(tableIndex, 0));
            
            try {
                AdminController.removeTechnician(technicianID);
            } catch (SQLException ex) {
                Logger.getLogger(gui_allTechnicians.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null,
                    "Technician with ID:  " + technicianID + " has been removed",
                    "The ticket is succefully canceled", JOptionPane.INFORMATION_MESSAGE);

            populateTable();
        } catch (IOException ex) {
            Logger.getLogger(gui_allCustomers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonRemoveCustomerActionPerformed

