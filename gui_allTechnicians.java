    public void populateTable() throws IOException {
        String columns[] = {"Technician ID",
            "Availablity",
            "Email",
            "Username",
            "Password",
            "Expertise",};
        String[][] data = new String[Technician.getAllTechnicians().size()][];
        for (int i = 0; i < Technician.getAllTechnicians().size(); i++) {
            Technician tech = Technician.getAllTechnicians().get(i);
            data[i] = new String[]{
                String.valueOf(tech.getUserID()),
                tech.getAvailability() ? "true" : "false",
                tech.getEmail(),
                tech.getUsername(),
                tech.getPassword(),
                tech.getExpertise(),
            };
        }
        DefaultTableModel model = new DefaultTableModel(data, columns);
        jTable1.setModel(model);

    }

