    public void populateTable() throws IOException {
        String columns[] = {"UserName",
            "Password",
            "Email",
            "Address",
            "Phone number",
            "Customer ID"
        };
        String[][] data = new String[Customer.getAllCustomers().size()][];
        for (int i = 0; i < Customer.getAllCustomers().size(); i++) {
            Customer cust = Customer.getAllCustomers().get(i);
            data[i] = new String[]{
                cust.getUsername(),
                cust.getPassword(),
                cust.getEmail(),
                cust.getAddress(),
                String.valueOf(cust.getPhoneNumber()),
                String.valueOf(cust.getUserID())
            };
        }
        DefaultTableModel model = new DefaultTableModel(data, columns);
        jTable1.setModel(model);

    }


    private void jButtonBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBackActionPerformed

        
        
        for (int count = 0; count < jTable1.getRowCount(); count++) {
            StringBuilder jsonBuilder = new StringBuilder("{");
            String username = (String) jTable1.getValueAt(count, 0);
            String password = (String) jTable1.getValueAt(count, 1);
            String email = (String) jTable1.getValueAt(count, 2);
            String address = (String) jTable1.getValueAt(count, 3);
            String phoneNumber = (String) jTable1.getValueAt(count, 4);
            int CustomerID = Integer.parseInt(jTable1.getValueAt(count, 5).toString());
            
            AdminController.modifyUserAccount(CustomerID, address, email, username, password, phoneNumber);
        }
    }//GEN-LAST:event_jButtonBackActionPerformed

