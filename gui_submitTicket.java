    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      // TODO add your handling code here:
    String PartName = (String)jComboBox1.getSelectedItem();
    String Prioritystr = (String)jComboBox2.getSelectedItem();
    String description = (String)jTextField1.getText();
    
    for (int i = 0; i < ComputerPart.getAllComputerParts().size(); i++) {
        String CompName = ComputerPart.getAllComputerParts().get(i).getName();
        if (CompName.equals(PartName)) {
            int PartID = ComputerPart.getAllComputerParts().get(i).getPartID();
            if (Prioritystr.equals("High")) {
                TicketController.submitTicket(Ticket.TicketPriority.HIGH, PartID, description);
            } else if (Prioritystr.equals("Low")) {
                TicketController.submitTicket(Ticket.TicketPriority.LOW, PartID, description);
            }
            
        }
       
    }
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

